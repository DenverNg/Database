CREATE TABLE GIAOVIEN
(
    MAGV CHAR(5),
    HOTEN NVARCHAR(40),
    LUONG FLOAT,
    PHAI NVARCHAR(3),
    NGSINH DATE,
    DIACHI NVARCHAR(100),
    GVQLCM CHAR(5),
    MABM NCHAR(5),

    CONSTRAINT PK_GIAOVIEN
    PRIMARY KEY(MAGV)
