CREATE TABLE GV_DT
(
    MAGV CHAR(5),
    DIENTHOAI CHAR(12),

    CONSTRAINT PK_GV_DT
    PRIMARY KEY(MAGV, DIENTHOAI)
)

CREATE TABLE BOMON
(
    MABM NCHAR(5),
    TENBM NVARCHAR(40),
    PHONG CHAR(5),
    DIENTHOAI CHAR(12),
    TRUONGBM CHAR(5),
    MAKHOA CHAR(4),
    NGAYNHANCHUC DATE,

    CONSTRAINT PK_BOMON
    PRIMARY KEY(MABM)
)

CREATE TABLE KHOA
(
    MAKHOA CHAR(4),
    TENKHOA NVARCHAR(40),
    NAMTL INT,
    PHONG CHAR(5),
    DIENTHOAI CHAR(12),
    TRUONGKHOA CHAR(5),
    NGAYNHANCHUC DATE,

    CONSTRAINT PK_KHOA
    PRIMARY KEY(MAKHOA)
)

CREATE TABLE DETAI
(
    MADT CHAR(3),
    TENDT NVARCHAR(100),
    CAPQL NVARCHAR(40),
    KINHPHI FLOAT,
    NGAYBD DATE,
    NGAYKT DATE,
    MACD NVARCHAR(4),
    GVCNDT CHAR(5),

    CONSTRAINT PK_DETAI
    PRIMARY KEY(MADT)
)

CREATE TABLE CHUDE
(
    MACD NVARCHAR(4),
    TENCD NVARCHAR(50),

    CONSTRAINT PK_CHUDE
    PRIMARY KEY(MACD)
)

CREATE TABLE CONGVIEC
(
    MADT CHAR(3),
    SOTT INT,
    TENCV NVARCHAR(40),
    NGAYBD DATE,
    NGAYKT DATE,

    CONSTRAINT PK_CONGVIEC
    PRIMARY KEY(MADT, SOTT)
)

CREATE TABLE THAMGIADT
(
    MAGV CHAR(5),
    MADT CHAR(3),
    STT INT,
    PHUCAP FLOAT,
    KETQUA NVARCHAR(40),

    CONSTRAINT PK_THAMGIADT
    PRIMARY KEY(MAGV, MADT, STT)
)

CREATE TABLE NGUOITHAN
(
    MAGV CHAR(5),
    TEN NVARCHAR(20),
    NGSINH DATE,
    PHAI NVARCHAR(3),

    CONSTRAINT PK_NGUOITHAN
    PRIMARY KEY(MAGV, TEN)
)

